	public void Dispose()
	{
		_realEncryptionProvider.Dispose();
		_realKeyManagement.Dispose();
	}

	private AuditLogEncryptionService CreateService() =>
		new(
			_encryptionProvider,
			_keyManagementProvider,
			Microsoft.Extensions.Options.Options.Create(_options),
			_logger);

	private AuditLogEncryptionService CreateRealService() =>
		new(
			_realEncryptionProvider,
			_realKeyManagement,
			Microsoft.Extensions.Options.Options.Create(_options),
			_logger);

	private static AuditEvent CreateAuditEvent() =>
		new()
		{
			EventId = "evt-" + Guid.NewGuid().ToString("N")[..8],
			EventType = AuditEventType.Authentication,
			Action = "Login",
			Outcome = AuditOutcome.Success,
			Timestamp = DateTimeOffset.UtcNow,
			ActorId = "user-123",
			ActorType = "User",
			ResourceId = "resource-1",
			ResourceType = "Session",
			IpAddress = "192.168.1.1"
		};

	private static AuditEvent CreateAuditEventWithReason(string reason) =>
		new()
		{
			EventId = "evt-" + Guid.NewGuid().ToString("N")[..8],
			EventType = AuditEventType.Authentication,
			Action = "Login",
			Outcome = AuditOutcome.Success,
			Timestamp = DateTimeOffset.UtcNow,
			ActorId = "user-123",
			ActorType = "User",
			ResourceId = "resource-1",
			ResourceType = "Session",
			IpAddress = "192.168.1.1",
			Reason = reason
		};
}
