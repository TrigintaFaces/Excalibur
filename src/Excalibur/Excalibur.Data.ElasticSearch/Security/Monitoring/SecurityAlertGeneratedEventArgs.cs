namespace Excalibur.Data.ElasticSearch.Security;

/// <summary>
/// Event args for security alert generation events.
/// </summary>
/// <remarks>
/// Initializes a new instance of the <see cref="SecurityAlertGeneratedEventArgs" /> class.
/// </remarks>
/// <param name="alert"> The security alert that was generated. </param>
/// <exception cref="ArgumentNullException"> Thrown when <paramref name="alert" /> is null. </exception>
public sealed class SecurityAlertGeneratedEventArgs(SecurityAlert alert) : EventArgs
{
	/// <summary>
	/// Gets the security alert that was generated.
	/// </summary>
	/// <value>
	/// A SecurityAlert object containing detailed information about the security alert that was generated by the monitoring system.
	/// </value>
	public SecurityAlert Alert { get; } = alert ?? throw new ArgumentNullException(nameof(alert));
}
